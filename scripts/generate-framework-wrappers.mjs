import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { componentDefinitions } from "./wrapper-manifest.mjs";

const dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.dirname(dirname);

async function main() {
  await writeGeneratedFile(path.join(repoRoot, "packages", "react", "src", "index.tsx"), generateReactFile());
  await writeGeneratedFile(path.join(repoRoot, "packages", "vue", "src", "index.ts"), generateVueFile());
}

function generateReactFile() {
  const imports = componentDefinitions
    .map((component) => `  ${component.exportName} as ${component.exportName}Element,`)
    .join("\n");

  const exports = componentDefinitions
    .map((component) => {
      const events = renderReactEvents(component.reactEvents ?? unique(component.vueHandlers.map((handler) => handler.domEvent)));

      return [
        `export const ${component.exportName} = createComponent({`,
        `  react: React,`,
        `  tagName: "${component.tagName}",`,
        `  elementClass: ${component.exportName}Element${events ? "," : ""}`,
        events ? `  events: ${events}` : "",
        `});`
      ]
        .filter(Boolean)
        .join("\n");
    })
    .join("\n\n");

  return `// AUTO-GENERATED BY npm run generate:wrappers. DO NOT EDIT MANUALLY.
import * as React from "react";
import { createComponent } from "@lit/react";

import {
${imports}
} from "cindor-ui-core";
export { clearToasts, dismissToast, ensureToastRegion, showToast } from "cindor-ui-core";
import "cindor-ui-core/register";

${exports}
`;
}

function generateVueFile() {
  const neededHostTypes = unique(
    componentDefinitions.flatMap((component) => component.vueHandlers.map((handler) => handler.modelHostType).filter(Boolean))
  );
  const neededCoreTypes = unique(
    componentDefinitions.flatMap((component) =>
      component.vueProps.flatMap((prop) => extractTypeIdentifiers(prop.typeExpression ?? ""))
    )
  ).sort();

  const typeImportBlock =
    neededCoreTypes.length > 0
      ? `import type {\n${neededCoreTypes.map((typeName) => `  ${typeName}`).join(",\n")}\n} from "cindor-ui-core";\n`
      : "";

  const hostTypes = [
    neededHostTypes.includes("InputHost") ? 'type InputHost = HTMLElement & { value: string };' : "",
    neededHostTypes.includes("CheckboxHost") ? 'type CheckboxHost = HTMLElement & { checked: boolean };' : "",
    neededHostTypes.includes("SelectHost") ? 'type SelectHost = HTMLElement & { value: string };' : "",
    neededHostTypes.includes("OpenHost") ? 'type OpenHost = HTMLElement & { open: boolean };' : "",
    neededHostTypes.includes("FileInputHost") ? 'type FileInputHost = HTMLElement & { files: FileList | null };' : "",
    neededHostTypes.includes("PageHost") ? 'type PageHost = HTMLElement & { currentPage: number };' : "",
    neededHostTypes.includes("SearchQueryHost") ? 'type SearchQueryHost = HTMLElement & { searchQuery: string };' : ""
  ]
    .filter(Boolean)
    .join("\n");

  const exports = componentDefinitions.map((component) => renderVueComponent(component)).join("\n\n");

  return `// AUTO-GENERATED BY npm run generate:wrappers. DO NOT EDIT MANUALLY.
import { defineComponent, h, type PropType } from "vue";

${typeImportBlock}export { clearToasts, dismissToast, ensureToastRegion, showToast } from "cindor-ui-core";
import "cindor-ui-core/register";

${hostTypes}

${exports}
`;
}

function renderReactEvents(domEvents) {
  if (domEvents.length === 0) {
    return "";
  }

  const lines = domEvents.map((domEvent) => `    ${toHandlerProp(domEvent)}: "${domEvent}"`);

  return `{\n${lines.join(",\n")}\n  }`;
}

function renderVueComponent(component) {
  const propsBlock = component.vueProps.length > 0 ? renderVueProps(component.vueProps) : "";
  const handlersBlock = component.vueHandlers.map((handler) => renderVueHandler(handler)).join("\n\n");
  const emits = unique(
    component.vueHandlers.flatMap((handler) => [handler.modelEmit, handler.emitName].filter(Boolean))
  );
  const emitsBlock = emits.length > 0 ? `  emits: [${emits.map((emitName) => `"${emitName}"`).join(", ")}],\n` : "";
  const usesProps = component.vueProps.length > 0;
  const usesSlots = component.slots !== "none";
  const usesEmit = component.vueHandlers.length > 0;

  const setupFirstParam = usesProps ? "props" : "_";
  const setupContext = ["attrs", usesEmit ? "emit" : "", usesSlots ? "slots" : ""].filter(Boolean).join(", ");
  const setupArgs = `${setupFirstParam}, { ${setupContext} }`;

  const renderLines = [
    `return () =>`,
    usesSlots
      ? `      h(\n        "${component.tagName}",\n        {\n${renderVueRenderProps(component)}\n        },\n        ${component.slots === "all" ? "slots" : "slots.default?.()"}\n      );`
      : `      h("${component.tagName}", {\n${renderVueRenderProps(component)}\n      });`
  ];

  return [
    `export const ${component.exportName} = defineComponent({`,
    `  name: "${component.exportName}",`,
    propsBlock ? `  props: ${propsBlock},` : "",
    emitsBlock.trimEnd(),
    `  setup(${setupArgs}) {`,
    handlersBlock ? indent(handlersBlock, 4) : "",
    indent(renderLines.join("\n"), 4),
    `  }`,
    `});`
  ]
    .filter(Boolean)
    .join("\n");
}

function renderVueProps(props) {
  return `{\n${props.map((prop) => `    ${prop.name}: ${renderVuePropDefinition(prop)}`).join(",\n")}\n  }`;
}

function renderVuePropDefinition(prop) {
  switch (prop.kind) {
    case "boolean":
      return `{ type: Boolean, default: ${String(prop.defaultValue)} }`;
    case "number":
      return `{ type: Number, default: ${String(prop.defaultValue)} }`;
    case "string":
      return `{ type: String, default: ${JSON.stringify(prop.defaultValue)} }`;
    case "typed-string":
      return `{ type: String as PropType<${prop.typeExpression}>, default: ${JSON.stringify(prop.defaultValue)} }`;
    case "array":
      return `{ type: Array as PropType<${prop.typeExpression}>, default: ${prop.defaultFactory} }`;
    case "object":
      return `{ type: Object as PropType<${prop.typeExpression}>, default: ${prop.defaultFactory} }`;
    default:
      throw new Error(`Unsupported prop kind: ${prop.kind}`);
  }
}

function renderVueHandler(handler) {
  const handlerName = toFunctionName(handler.domEvent);
  const lines = [`const ${handlerName} = (event: Event) => {`];

  if (handler.modelHostType && handler.modelHostProperty && !handler.modelValueExpression) {
    lines.push(`  const target = event.currentTarget as ${handler.modelHostType};`);
  } else if (handler.modelHostType && handler.modelValueExpression?.includes("target.")) {
    lines.push(`  const target = event.currentTarget as ${handler.modelHostType};`);
  }

  if (handler.modelEmit) {
    const valueExpression =
      handler.modelValueExpression ??
      (handler.modelHostProperty ? `target.${handler.modelHostProperty}` : undefined) ??
      "undefined";
    lines.push(`  emit("${handler.modelEmit}", ${valueExpression});`);
  }

  if (handler.emitName) {
    lines.push(`  emit("${handler.emitName}", event);`);
  }

  lines.push(`};`);
  return lines.join("\n");
}

function renderVueRenderProps(component) {
  const propLines = component.vueProps.map((prop) => `          ${renderVueObjectKey(prop.attr)}: ${renderVuePropValue(prop)},`);
  const eventLines = component.vueHandlers.map(
    (handler) => `          ${toHandlerProp(handler.domEvent)}: ${toFunctionName(handler.domEvent)},`
  );

  return ["          ...attrs,", ...propLines, ...eventLines].join("\n");
}

function renderVueObjectKey(key) {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(key) ? key : JSON.stringify(key);
}

function renderVuePropValue(prop) {
  switch (prop.kind) {
    case "boolean":
      return `props.${prop.name} || undefined`;
    case "number":
      return `props.${prop.name}`;
    case "string":
      return prop.alwaysPass ? `props.${prop.name}` : `props.${prop.name} || undefined`;
    case "typed-string":
      return prop.alwaysPass ? `props.${prop.name}` : `props.${prop.name} || undefined`;
    case "array":
      return `props.${prop.name}`;
    case "object":
      return `props.${prop.name}`;
    default:
      throw new Error(`Unsupported prop kind: ${prop.kind}`);
  }
}

function extractTypeIdentifiers(typeExpression) {
  return Array.from(typeExpression.matchAll(/\b[A-Z][A-Za-z0-9_]*/g)).map((match) => match[0]);
}

function indent(value, spaces) {
  const prefix = " ".repeat(spaces);
  return value
    .split("\n")
    .map((line) => (line === "" ? line : `${prefix}${line}`))
    .join("\n");
}

function toFunctionName(domEvent) {
  return `handle${domEvent
    .split("-")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("")}`;
}

function toHandlerProp(domEvent) {
  return `on${domEvent
    .split("-")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("")}`;
}

function unique(values) {
  return Array.from(new Set(values.filter(Boolean)));
}

async function writeGeneratedFile(filePath, content) {
  await mkdir(path.dirname(filePath), { recursive: true });
  await writeFile(filePath, content, "utf8");
}

await main();
